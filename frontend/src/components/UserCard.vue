<template>
  <div class="user-card">
    <h2 class="user-id">User ID: {{ userId }}</h2>
    <p class="user-name">Name: {{ name }}</p>
    <button class="details-button" @click="goDetail">Details</button>
  </div>
</template>

<script>
export default {
  name: "UserCard",
  props: {
    userId: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
  },
  methods: {
    goDetail() {
      if (this.userId) {
        this.$router.push({ name: "/user-detail", params: { userId: this.userId } });
      } else {
        console.error("userId is not defined in UserCard.vue");
      }
    },
  }
};
</script>

